<script>
  // Example cart items (this can be retrieved from local storage, session, or API in a real app)
  const cartItems = [
    { name: "Wedding Dress", price: 250, quantity: 1 },
    { name: "Groom Suit", price: 150, quantity: 1 },
    { name: "Flower Bouquet", price: 50, quantity: 1 }
  ];

  // Function to render checkout items
  function renderCheckoutItems() {
    const checkoutContainer = document.getElementById('checkout-items');
    let totalPrice = 0;

    cartItems.forEach(item => {
      totalPrice += item.price * item.quantity;
      const itemDiv = document.createElement('div');
      itemDiv.classList.add('flex', 'justify-between', 'items-center', 'mb-4');
      itemDiv.innerHTML = `
        <span class="font-medium text-gray-700">${item.name} x ${item.quantity}</span>
        <span class="text-gray-600">$${(item.price * item.quantity).toFixed(2)}</span>
      `;
      checkoutContainer.appendChild(itemDiv);
    });

    // Update total price
    document.getElementById('checkout-total').textContent = totalPrice.toFixed(2);
  }

  // Render checkout items on page load
  window.onload = renderCheckoutItems;
</script>
